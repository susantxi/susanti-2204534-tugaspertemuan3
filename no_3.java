package java_beginning;

public class no_3 {
    public static void main(String[] args) {
        int asciiX = (int) 'X';
        int asciiy = (int) 'y';
        
        int totalAscii = asciiX + asciiy;
        
        System.out.println("ASCII X = " + asciiX);
        System.out.println("ASCII y = " + asciiy);
        System.out.println("Hasil penjumlahan kode ASCII dari karakter 'X' dan 'y' adalah: " + totalAscii);
    }
}
